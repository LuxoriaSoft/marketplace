namespace Luxoria.Modules.Models
{
    /// <summary>
    /// Represents a file extension.
    /// </summary>
    public enum FileExtension
    {
        /// <summary>
        /// Represents an unknown file extension.
        /// </summary>
        UNKNOWN,
        /// <summary>
        /// Represents a PNG file extension.
        /// </summary>
        PNG,
        /// <summary>
        /// Represents a JPG file extension.
        /// </summary>
        JPG,
        /// <summary>
        /// Represents a JPEG file extension.
        /// </summary>
        JPEG,
        /// <summary>
        /// Represents a RAW file extension.
        /// </summary>
        RAW,
        /// <summary>
        /// Represents an ARW file extension.
        /// </summary>
        ARW
    }
}